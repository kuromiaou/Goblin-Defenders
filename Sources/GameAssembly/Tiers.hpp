#pragma once

// ============================================================
// DAMAGE TYPE
// ============================================================
enum class DamageType { PHYSIQUE, MAGIQUE };

// ============================================================
// HP TIER
// ============================================================
enum class HPTier { HP_HIGH = 50, HP_MID = 25, HP_LOW = 10 };

// ============================================================
// ATK TIER
// ============================================================
enum class ATKTier { ATK_HIGH = 25, ATK_MID = 10, ATK_LOW = 5, ATK_NONE = 0 };

// ============================================================
// SPD TIER (x0.1 = m/s => SPD_HIGH=1.5, SPD_MID=1.0, SPD_LOW=0.5)
// ============================================================
enum class SPDTier { SPD_HIGH = 15, SPD_MID = 10, SPD_LOW = 5, SPD_NONE = 0 };

// ============================================================
// ATK_SPD TIER (x0.1 => ATKSPD_HIGH=1.0, ATKSPD_LOW=0.75)
// ============================================================
enum class ATKSPDTier { ATKSPD_HIGH = 10, ATKSPD_LOW = 7, ATKSPD_NONE = 0 };

// ============================================================
// ATK_RANGE TIER (Enemy / Player)
// ============================================================
enum class ATKRangeTier { MELEE = 1, RANGE = 3 };

// ============================================================
// RESISTANCE TIER — formule : 100 / (100 + res)
//   RES_HIGH = 100 => 50% reduction
//   RES_MID  = 33  => ~25% reduction
//   RES_LOW  = 0   =>  0% reduction
// ============================================================
enum class ResTier { RES_HIGH = 100, RES_MID = 33, RES_LOW = 0 };

// ============================================================
// TOWER ATK_RANGE TIER
// ============================================================
enum class TowerRangeTier { TOWER_HIGH = 5, TOWER_MID = 3, TOWER_LOW = 1 };

// ============================================================
// AGGRO MODE (Tower targeting)
// ============================================================
enum class AggroMode { FIRST, LAST, MAX_HP, CURRENT_HP };

// ============================================================
// SHAMAN AURA TYPE
// ============================================================
enum class AuraType { ATK_BOOST, SPD_BOOST, RES_BOOST };

// ============================================================
// CC TYPE
// ============================================================
enum class CCType { SLOW, SHRED };
